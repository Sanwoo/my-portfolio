const Circle = forwardRef<
  HTMLDivElement,
  {
    className?: string;
    children?: React.ReactNode;
    onClick?: React.MouseEventHandler<HTMLDivElement>;
  }
>(({ className, children, onClick }, ref) => {
  return (
    <motion.div
      ref={ref}
      onClick={onClick}
      className={cn(
        "z-10 flex size-16 md:size-24 items-center justify-center rounded-full border-2 bg-white p-2 md:p-3 shadow-[0_0_20px_-12px_rgba(0,0,0,0.8)] hover:scale-110 duration-300 hover:cursor-pointer",
        className
      )}
    >
      {children}
    </motion.div>
  );
});

Circle.displayName = "Circle";

